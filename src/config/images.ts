/**
 * 从预定义的图片列表中随机选择指定数量的图片
 * @param count - 需要选择的图片数量
 * @returns - 随机选择的图片URL数组
 */
export const getRandomPaintings = (count: number = 10): string[] => {
  const shuffled = [...FAMOUS_PAINTINGS].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, Math.min(count, FAMOUS_PAINTINGS.length))
}
